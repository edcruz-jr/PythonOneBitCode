num1 = float(input("Digite o primeiro número: "))
antecessor = num1 - 1
sucessor = num1 + 1

print(f"O número digitado foi {num1}, o seu antecessor é {antecessor} e seu sucessor é {sucessor}")

num2 = float(input("Digite o segundo número: "))
num3 = float(input("Digite o terceciro número: "))
num4 = float(input("Digite o quarto número: "))

media = (num1 + num2 + num3 + num4) / 4

print(f"O números digitados foram {num1}, {num2}, {num3}, {num4} e a média entre eles é {media}")