# Exercício 1 - Antecessor e Sucessor
num1 = float(input("Digite o primeiro número: "))
antecessor = num1 - 1
sucessor = num1 + 1

print(f"O número digitado foi {num1}, o seu antecessor é {antecessor} e seu sucessor é {sucessor}")

note1 = float(input("Digite o primeiro número: "))
note2 = float(input("Digite o segundo número: "))
note3 = float(input("Digite o terceciro número: "))
note4 = float(input("Digite o quarto número: "))

media = (note1 + note2 + note3 + note4) / 4

print(f"O números digitados foram {note1}, {note2}, {note3}, {note4} e a média entre eles é {media}")